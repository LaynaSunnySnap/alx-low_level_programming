#include "main.h"

/**
 * main - Entry level
 * print_alphabet: prints alphabets in lowercase followed by a new line
 * print_alphabet - prints alphabets in lowercase followed by a new line
 * 
 * print_alphabet - print alphabets
 * Return: void
 */

void print_alphabet(void)
{
char ch;
for (ch = 'a'; ch <= 'z'; ch++)
_putchar(ch);
_putchar('\n');
}
